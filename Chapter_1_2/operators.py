## Math Operators from Highest to Lowest Precedendce

# Exponent (**)
2 ** 3 # 8

# Modulus/Remainder (%)
22 % 8 # 6

# Integer division (//)
22 // 8 # 2

# Division (/)
22 / 8 # 2.75

# Multiplication (*)
3 * 5 # 15

# Subtraction (-)
5 - 2 # 3

# Addition (+)
2 + 2 # 4



